	HAL_Delay(10);

	// Obtain accelerometer and gyro data
	ICM_ReadAccelGyro();

	// Obtain magnetometer data
	int16_t mag_data[3];
	ICM_ReadMag(mag_data);

//	ppgunsigned16 = 15;
//	//Read PPG sensor data
//	if(MAX86150_check() > 0) {
//			ppgunsigned16 = (uint16_t) (MAX86150_getFIFORed()>>2);
